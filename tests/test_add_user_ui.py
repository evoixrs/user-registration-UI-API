import time

def test_add_new_user_success(login_page, add_user_page, registered_user):
    """
    Проверяем позитивный сценарий добавления нового пользователя.

    Что хотим проверить в этом тесте:
    1. Зарегистрированный пользователь может успешно войти в систему.
    2. После входа открывается страница списка пользователей.
    3. Со страницы списка пользователей можно перейти
       на страницу добавления пользователя.
    4. Новый пользователь успешно сохраняется через UI.
    5. После сохранения новый пользователь отображается
       в таблице пользователей.
    """

    # Подготавливаем тестовые данные для авторизации.
    # Фикстура registered_user заранее создает валидного пользователя через API.
    # Но в зависимости от реализации она может вернуть:
    # 1. словарь;
    # 2. объект модели.
    #
    # Поэтому ниже делаем универсальную обработку данных,
    # чтобы тест не зависел от конкретного типа возвращаемого значения.
    if isinstance(registered_user, dict):
        # Если получили словарь,
        # достаем логин по ключу "login".
        login = registered_user["login"]

        # Если получили словарь,
        # достаем пароль по ключу "password".
        password = registered_user["password"]
    else:
        # Если получили объект,
        # достаем логин как атрибут объекта.
        login = registered_user.login

        # Если получили объект,
        # достаем пароль как атрибут объекта.
        password = registered_user.password

    # Подготавливаем тестовые данные для нового пользователя,
    # которого будем добавлять через UI.
    #
    # Имя делаем уникальным через time.time(),
    # чтобы:
    # 1. не конфликтовать с уже существующими пользователями;
    # 2. легко найти нового пользователя в таблице;
    # 3. не получать ложные совпадения при повторных прогонах.
    new_user_name = f"UI User {int(time.time())}"

    # Возраст передаем строкой,
    # потому что поле формы заполняется через send_keys.
    # Для позитивного сценария берем простое валидное значение.
    new_user_age = "28"

    # Пол передаем строкой,
    # так как в текущей форме это обычное текстовое поле.
    new_user_gender = "М"

    # Дату рождения передаем в формате,
    # который подсказывает сама форма: дд.мм.гггг.
    # Для поля даты в page object используем execute_script,
    # а не обычный send_keys.
    new_user_birthday = "02.02.1991"

    # Выполняем позитивный сценарий логина:
    # 1. переходим на страницу входа;
    # 2. вводим логин;
    # 3. вводим пароль;
    # 4. нажимаем кнопку входа.
    login_page.login(login, password)

    # Проверяем, что после успешной авторизации
    # действительно открылась страница списка пользователей.
    assert login_page.is_users_page_opened()

    # Переходим со страницы списка пользователей
    # на страницу добавления пользователя
    # через верхнее меню "Добавить пользователя".
    add_user_page.open_add_user_page()

    # Дополнительно убеждаемся,
    # что форма добавления пользователя действительно открылась.
    assert add_user_page.is_add_user_page_opened()

    # Выполняем основной шаг теста:
    # заполняем форму нового пользователя
    # и сохраняем его.
    add_user_page.add_user(
        name=new_user_name,
        age=new_user_age,
        gender=new_user_gender,
        birthday=new_user_birthday,
        is_active=True
    )

    # После успешного сохранения пользователя
    # переходим к таблице пользователей.
    add_user_page.go_to_users_table()

    # Проверяем, что после перехода
    # снова открылась страница списка пользователей.
    assert add_user_page.is_users_page_opened()

    # Проверяем главный результат теста:
    # новый пользователь должен появиться в таблице.
    assert add_user_page.is_user_present_in_table(new_user_name)